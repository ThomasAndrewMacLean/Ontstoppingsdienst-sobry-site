    <h3>Ontstoppen met een PET-flesr</h3>
    <p>
        Met een PET-fles kunt u kleine verstoppingen verwijderen. Meestal is een fles van twee liter al voldoende. Knip
        de onderkant van de fles af en plaatst deze in de wc-hals. sluit wel de dat de PET-fles af met de dop. Door het
        op en neer bewegen, maakt u de verstopping los. Indien in het water bubbels tevoorschijn komen, dan bent u goed
        bezig en maakt u de verstopping los. Vergeet achteraf uw wc niet een paar keer goed door te spoelen. Werkt het
