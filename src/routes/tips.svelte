<h1>Tips</h1>
<h2>Tips om een wc snel en makkelijk te ontstoppen</h2>

<p>
 Het is geen prettige situatie, maar een verstopte wc komt helaas nog maar al te vaak voor. 
 Waarschijnlijk staat u al met de telefoon in de hand, klaar om de loodgieter te bellen. Een wc is per
slot van rekening onmisbaar. Wat u vooral niet moet doen is in paniek raken en de wc nogmaals doorspoelen. Dit zal
alleen maar meer wateroverlast veroorzaken.</p>

<p>Voordat u echter de loodgieter belt, zijn er een aantal handelingen die u
zelf kunt uitvoeren waarmee u zelf uw probleem op kunt lossen. En hiervoor gebruikt u middelen die iedereen vaak al in
huis heeft staan.</p> 
<h3>Verstopping handmatig verwijderen</h3>
<p> Soms is de makkelijkste manier om de wc te ontstoppen simpelweg door
een paar goede handschoenen aan te trekken en in de hals van de wc te voelen of hier een voorwerp klem zit dat de
verstopping veroorzaakt. Als u kinderen heeft dan bent u waarschijnlijk op de hoogte dat deze graag speelgoed in de wc
gooien. Ook kan het zijn dat er een wc-rol vastzit omdat deze per ongeluk in de wc is gevallen. Verwijder deze simpelweg
handmatig en uw probleem is verholpen.</p> 
<h3>Afwasmiddel en heet water</h3> 
<p>Een andere erg simpele manier om een verstopping te
verhelpen is met afwasmiddel en heet water. Dit is voornamelijk handig als de verstopping wordt veroorzaakt door iets
vettigs. U doet een paar kneepjes afwasmiddel in de wc-hals. Meer is niet nodig. Vervolgens haalt u een halve emmer heet
water en giet dit vanaf minstens een meter hoogte de wc in. Zorg ervoor dat het water niet kookt, want dit kan uw
porselein beschadigen. Het gieten vanaf hoogte geeft extra druk. Nu laat u dit hete water en het afwasmiddel vijf
minuten inwerken. Als de verstopping niet te ernstig is, zal het afwasmiddel dit verzachten, oplossen of losweken en
kunt u de wc weer gewoon gaan gebruiken.</p> 
<h3>Ontstopper (of gebruik een PET-fles)</h3>
<p> In elke doe-het-zelf zaak kunt u een
simpele ontstopper kopen. Waarschijnlijk heeft u er nog wel een in huis. Als u van tevoren het rubber afspoelt onder
heet water zal dit soepeler worden er de hals van de wc beter afsluiten. Plaats de ontstopper in het water en in de
wc-hals. Sluit deze niet goed af, dan kunt u er een oude doek omheen wikkelen. Vervolgens gaat u de ontstopper op en
neer bewegen zodat er druk ontstaat op de wc-hals. Zo kunt u de verstopping losmaken en zal het automatisch worden
afgevoerd. Heeft u geen ontstopper in huis, maar wel een PET-fles, dan heeft u geluk. Kleine ontstoppingen kunt u
namelijk ook met een 2 literfles verwijderen. U knipt de onderkant van de fles af en plaatst deze in de wc-hals. Zorg
ervoor dat de PET-fles is afgesloten met de dop. Nu kunt u de fles op en neer bewegen, wederom om druk te creÃ«ren en de
verstopping los te maken. Ziet u bubbelend water, dan gaat het de goede kant op en komt de verstopping los. Nu spoelt u
het toilet een paar keer door en u kunt er weer gewoon gebruik van maken.</p>
<h3>Sodakristallen(verkrijgbaar in supermarkt) en
azijn</h3> <p>Sodakristallen geeft samen met azijn een chemische reactie en deze reactie kan ervoor zorgen dat uw toilet weer
zal worden ontstopt. Strooi eerst een pak Sodakristallen in het toilet en giet hier een liter azijn overheen. Het water
zal gaan bruisen en schuimen en dit is de reactie van de soda op de azijn. Dit laat u een paar minuten inwerken alvorens
u een emmer heet water in het toilet giet. U laat uw toilet vervolgens een nacht met rust. Deze methode werkt alleen met
organische verstoppingen. Hopelijk kunt u het toilet weer gewoon gebruiken als u de volgende ochtend opstaat.</p>

<h3>Chemisch
ontstoppingsmiddel</h3>
<p>Als al de bovenstaande middelen niet werken kunt u bij de meeste supermarkten, drogisten of
doe-het-zelf zaken een chemisch ontstoppingsmiddel aanschaffen. Dit zou uw laatste redmiddel moeten zijn omdat u zo wel
een schadelijk middel het milieu inbrengt. Deze middelen verwijderen op een agressieve manier organische opstoppingen
van bijvoorbeeld haren of toiletpapier en zijn hierin zeer effectief. U kunt ook overwegen een natuurlijk
wc-ontstoppingsmiddel aan te schaffen. Houd er wel rekening mee dat deze een langere tijd nodig hebben om te werken.</p>


