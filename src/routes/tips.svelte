    <h3>Uw verstopping verhelpen met een ontstopper</h3>
    <p>
        Het is altijd handig om een eenvoudige ontstopper in huis te hebben. Voor het gebruik van de ontstopper, zorg
        dat u eerst het rubber afspoelt met heet water zodat het rubber van uw ontstopper soepel wordt. Plaats de
        ontstopper in de hals van uw wc. Indien het niet goed afsluit, wikkel dan een oude doek om uw ontstopper. Door
        uw ontstopper op en neer te laten bewegen instaat er een druk op uw wc- hals en verhelpt u de verstopping. Werkt
